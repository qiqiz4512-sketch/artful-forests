/** Get persona label for display in chat UI */
export function getPersonaLabel(personality: string): string {
  return PERSONA_MATRIX[personality as PersonaKey]?.label ?? '林间居民';
}

/** Pick a random persona-flavoured emoji */
export function pickPersonaEmoji(personality: string): string {
  const profile = PERSONA_MATRIX[personality as PersonaKey];
  if (!profile) return '🌿';
  const { emojis } = profile;
  return emojis[Math.floor(Math.random() * emojis.length)];
}

/** Generate virtual interaction counts biased by personality */
export function genVirtualInteractions(personality: string): { likes: number; comments: number } {
  switch (personality) {
    case '活泼': return { likes: 5 + Math.floor(Math.random() * 28), comments: 3 + Math.floor(Math.random() * 14) };
    case '调皮': return { likes: 3 + Math.floor(Math.random() * 18), comments: 4 + Math.floor(Math.random() * 18) };
    case '温柔': return { likes: 4 + Math.floor(Math.random() * 16), comments: 2 + Math.floor(Math.random() * 10) };
    case '睿智': return { likes: 2 + Math.floor(Math.random() * 10), comments: 5 + Math.floor(Math.random() * 22) };
    case '社恐': return { likes: 1 + Math.floor(Math.random() * 7),  comments: 0 + Math.floor(Math.random() * 4) };
    case '神启': return { likes: 12 + Math.floor(Math.random() * 38), comments: 8 + Math.floor(Math.random() * 28) };
    default:     return { likes: 1 + Math.floor(Math.random() * 8),   comments: 1 + Math.floor(Math.random() * 6) };
  }
}
