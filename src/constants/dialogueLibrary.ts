const CANONICAL_TONE: Record<string, DialogueTone> = {
  温柔: '温柔',
  睿智: '睿智',
  活泼: '活泼',
  社恐: '社恐',
  调皮: '调皮',
  顽皮: '调皮',
};

export function resolveDialogueTone(personality: string): DialogueTone {
  return CANONICAL_TONE[personality] ?? '温柔';
}

export function pickWithRandomOffset(lines: string[]): string {
  if (lines.length === 0) return '';
  const start = Math.floor(Math.random() * lines.length);
  const drift = Math.floor((Math.random() - 0.5) * 6);
  const index = (start + drift + lines.length) % lines.length;
  return lines[index];
}

export function buildReceiverFeedback(sender: TreeAgent, receiver: TreeAgent): string {
  const senderTone = resolveDialogueTone(sender.personality);
  const receiverTone = resolveDialogueTone(receiver.personality);
  const fromMatrix = PERSONALITY_PAIR_MATRIX[senderTone]?.[receiverTone] ?? [];
  if (fromMatrix.length > 0) return pickWithRandomOffset(fromMatrix);
  return pickWithRandomOffset(PERSONALITY_DICTIONARY[receiverTone]);
}
